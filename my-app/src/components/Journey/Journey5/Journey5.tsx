const Journey5 = ({ onNext, onData }: { onNext: () => void; onData: (data: any) => void }) => {
  const [selectedState, setSelectedState] = useState<string>('');
  const [districtOptions, setDistrictOptions] = useState<string[]>([]);


  const handleNext = () => {
    if (selectedState && districtOptions.length > 0) {
      const selectedDistrict = (document.getElementById('inputDistrict') as HTMLSelectElement)?.value || '';
      // Send the selected district to the parent component using onData callback
      onData({ selectedState, selectedDistrict });
      onNext();
    } else {
      alert('Please select a state and district before proceeding.');
    }
  };


  useEffect(() => {
    if (selectedState === 'Select State') {
      setDistrictOptions([]);
    } else {
      setDistrictOptions(districtData[selectedState] || []);
    }
  }, [selectedState]);

  const handleStateChange = (e: React.ChangeEvent<HTMLSelectElement>) => {
    setSelectedState(e.target.value);
  };

  return (
    <div className="formContainer">
      <form>
        <div className="form-group col-md-4">
          <label htmlFor="inputState" className='j5-input'>State</label>
          <select
            className="form-control"
            id="inputState"
            onChange={handleStateChange}
            value={selectedState}
          >
            {stateOptions.map((state) => (
              <option key={state} value={state}>
                {state}
              </option>
            ))}
          </select>
        </div>
        <div className="form-group col-md-4">
          <label htmlFor="inputDistrict" className='j5-input'>District</label>
          <select className="form-control" id="inputDistrict">
            <option value="">-- Select one --</option>
            {districtOptions.map((district) => (
              <option key={district} value={district}>
                {district}
              </option>
            ))}
          </select>
        </div>
        <button onClick={handleNext} className='j5-btn'>Next</button>
      </form>
    </div>
  );
};

export default Journey5;
